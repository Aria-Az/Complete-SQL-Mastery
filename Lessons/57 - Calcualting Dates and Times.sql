-- Calcualting Dates and Times

SELECT DATE_ADD(NOW(), INTERVAL 1 DAY) -- RETURNS TOMMOROW DATE
SELECT DATE_ADD(NOW(), INTERVAL 1 YEAR) -- RETURNS NEXT YEAR SAME DAY
SELECT DATE_ADD(NOW(), INTERVAL -1 YEAR) -- RETURNS LAST YEAR
SELECT DATE_SUB(NOW(), INTERVAL 1 YEAR) -- RETURNS LAST YEAR

SELECT DATEDIFF('2019-01-05', '2019-01-01') -- 4
SELECT DATEDIFF('2019-01-05 9:00', '2019-01-01 17:00') -- STILL 4
SELECT DATEDIFF('2019-01-01 17:00', '2019-01-05 9:00') -- -4

SELECT TIME_TO_SEC('09:00') -- 32400
SELECT TIME_TO_SEC('09:00') - TIME_TO_SEC('09:02') -- -120
